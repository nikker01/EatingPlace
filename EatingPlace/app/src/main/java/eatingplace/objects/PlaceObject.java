public class PlaceObject {

    String placeIconUrl;
    String placeName;
    String placeId;
    String placeRating;
    String placeRef;
    String placeVicinity;
    String placeAddress;

    public String getPlaceAddress() {
        return placeAddress;
    }

    public void setPlaceAddress(String placeAddress) {
        this.placeAddress = placeAddress;
    }

    public String getPlaceIconUrl() {
        return placeIconUrl;
    }

    public void setPlaceIconUrl(String placeIconUrl) {
        this.placeIconUrl = placeIconUrl;
    }

    public String getPlaceName() {
        return placeName;
    }

    public void setPlaceName(String placeName) {
        this.placeName = placeName;
    }

    public String getPlaceId() {
        return placeId;
    }

    public void setPlaceId(String placeId) {
        this.placeId = placeId;
    }

    public String getPlaceRating() {
        return placeRating;
    }

    public void setPlaceRating(String placeRating) {
        this.placeRating = placeRating;
    }

    public String getPlaceRef() {
        return placeRef;
    }

    public void setPlaceRef(String placeRef) {
        this.placeRef = placeRef;
    }

    public String getPlaceVicinity() {
        return placeVicinity;
    }

    public void setPlaceVicinity(String placeVicinity) {
        this.placeVicinity = placeVicinity;
    }
}
